/** Spanning Tree and Minimum Spanning Tree
 * Undirected Graph - An undirected graph is a graph in whihc the edges do not point in any direction
 * i.e. the edges are bidirectional
 * 
 * Connected Graph - A connected graph is a graph in which there is always a path from a vertex to any other vertex.
 * 
 * Spanning Tree - A spanning tree is a sub-graph of an undirected connected graph,
 * which includes all the vertices of the graph with a minimum possible number of edges.
 * If a vertex is missed, then it is not spanning tree.
 * The edges may or may not have weights assigned to them.
 * 
 * Note:- The total number of spanning trees with N vertices that can be created from a complete graph is equal to N^(N-2)
 * 
 * Minimum Spanning tree - A minimum spanning tree is a spanning tree in which the sum of the weight of the edges is as minimum as possible.
 * The minimum spanning tree from a graph is found using the following  algorithms-
 * 1. Prim's Algorithm
 * 2. Kruskal's Algorithm
 * 
 * Spanning Tree Applications
 * 1. Computer Network Routing Protocol
 * 2. Cluster Analysis
 * 3. Civil Network Planning
 * 
 * Minimum Spanning Tree Applications
 * 1. To find paths in the map
 * 2. To design network like telecommunication networks, water supply networks, and electrical grids
 * 
 * 
 * Strongly Connected Components
 * A strongly connected component is the portion of a directed graph in which there is a path from each vertex to another vertex.
 * It is applicable only on a directed graph.
 * These components can be found using "Kosaraju's Algorithm"
 */